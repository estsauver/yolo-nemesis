function dy = silico(t,y)
global Feed
%*******************************************************
%  y(1) : X dimensionless cell mass concentration
%  y(2) : S dimensionless substrate concentration
%*******************************************************
Km=0.25; Ki=1.0; delT=1.0;
%*******************************************************
F=Feed(floor(t)+1);
%*******************************************************
mu = y(2)/(Km + y(2)+y(2)*y(2)/Ki);
%*******************************************************
dy=zeros(2,1);
dy(1)=mu*y(1)-F*y(1);
dy(2)=-mu*y(1)+F-F*y(2);
